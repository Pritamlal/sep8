public class pattern9 {
    public static void main(String[] args) {
        int n=5;
        int count=0;
        int i,j;

        for(i=1;i<=n;i++){
            for(j=1;j<=n;j++){
                count++;
                System.out.print(count+" ");
            }
            System.out.println(); 
        }
        
       
    }
}
